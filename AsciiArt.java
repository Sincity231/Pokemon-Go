    //static method for reading first ascii art from file and displaying it line-by-line via while loop
    public static String player2AsciiArt() throws FileNotFoundException {
        Scanner file = new Scanner(new File("AsciiArt"));
        String strBuilder = "";
        while (file.hasNext()) {
            String l1 = file.nextLine();
            for(int i=0; i<l1.length(); i++) {
                strBuilder+=l1.charAt(i);
                if (i+1 == l1.length()){
                    strBuilder+="\n";
                }
            }
        }
        return strBuilder;
    }
